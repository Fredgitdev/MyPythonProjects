#Arithmetic operators
x = 10
y = 4

print(x + y)
print(x - y)
print(x * y)
print(x % y) #Returns the remainder

#Comparison operators
print(x > y)
print(x < y)
print(x <= y)
print(x >= y)
print(x == y)
print(x != y)


#Assignment operator
w = 7
y = 12
print(w)

#Addition assignment operator

w += 2
print(w)

y -=4
print(y)

y /=4
print(y)

#Logic Operators - And, not, or

print(34 < 4 and 56 > 21)
print(34 < 4 or 56 > 21)
print(not(34 < 4 and 56 > 21))


