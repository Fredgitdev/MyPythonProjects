#while loop
number = 10
while number <=  20:
    print(number)
    number += 1

#Decrement
count = 105
while count >= 100:
    print("number is", count)
    count -= 1

#for loop

for x in range(10):
    print(x)


for num in range(15, 20):
    print(num)

for b in range(1, 10, 2):
    print(b)

#for loop in a list

languages = {"Python", "PHP", "Kotlin"}
for lang in languages:
    print(lang)